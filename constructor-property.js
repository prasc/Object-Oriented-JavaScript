// Constructor property
// Every object in JS has a property called constructor which referecnes the function that was used to create that object


// For example let's say we were using object literal syntax and did this

let x = {};

// internally, JS is going:

let x = new Object();

// There are other constructors in JS

new String();   // '', ""
new Boolean(); // true, false
new Number(); // 1, 2, 3, ...





